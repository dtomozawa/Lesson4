
public class IfElseSample1 {

    public static void main(String[] args) {
    int score =98;
    if (score==100) {
        System.out.println("満点です。おめでとうございます。");
    }else if(score>=80) {
        System.out.println("よくできました！！Very Good");
    }else if(score>=60) {
        System.out.println("合格です！");
    }else {
        System.out.println("補講を実施します");
    
    }

    }

}
